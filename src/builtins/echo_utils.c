#include "../../incl/minishell.h"

int	check_n(char *s)
{
	int	i;

	i = 0;
	if (s[i] != '-')
		return (0);
	while (s[++i])
	{
		if (s[i] != 'n')
		{
			if (s[i] == '\0' || s[i] == ' ')
				return (1);
			return (0);
		}
	}
	return (1);
}
